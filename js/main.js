$(function () {
    var messages = [], 
        index = 0;
    messages.push('Creative');
    messages.push('Ambitious');
    messages.push('Enthusiastic');
    function cycle() {
         
        $('#txtanim').html(messages[index]);
        index++;

        if (index == messages.length) {
            index = 0;
        }

        setTimeout(cycle, 1500);
    }

    cycle();
});